import streamlit as st

def show_typing_realtime(message="  拽..."):
    """
    爪 拽爪转 注 转  砖 驻砖 转砖.
    专 转 placeholder 砖转 拽 专 转专.
    """
    placeholder = st.empty()
    placeholder.markdown(f"<div style='color:gray; font-style:italic;'>{message}</div>", unsafe_allow_html=True)
    return placeholder
